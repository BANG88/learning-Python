# Define variables with different data types
a = 123  # integer
b = 1.23  # float
c = '123'  # string
d = True  # boolean
e = 3 + 4j  # complex number

# Print the values and types of the variables
print(a, type(a))
print(b, type(b))
print(c, type(c))
print(d, type(d))
print(e, type(e))

# Update the value of variable e
e = 'hello'

# Print the updated value and type of variable e
print(e, type(e))